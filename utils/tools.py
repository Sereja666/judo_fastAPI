

def format_names(text):
    """
    Преобразует текст с нумерованным списком имен в формат ['Фамилия И', ...]

    Args:
        text (str): Исходный текст с нумерованным списком имен

    Returns:
        list: Список отформатированных имен в формате 'Фамилия И'
    """
    lines = text.strip().split('\n')
    formatted_names = []

    for line in lines:
        # Убираем нумерацию в начале строки (цифры и точку)
        name_part = line.split('. ', 1)[-1] if '. ' in line else line

        # Убираем комментарии в скобках
        name_part = name_part.split(' (')[0]

        # Разделяем фамилию и имя
        parts = name_part.strip().split(' ')
        if len(parts) >= 2:
            surname = parts[0]
            first_letter = parts[1][0]  # Первая буква имени
            formatted_names.append(f"{surname} {first_letter}")

    return formatted_names



